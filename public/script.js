        async function loadAndSortGames() {
        const grid = document.getElementById('games-grid-container');
        if (!grid) return;

        try {
            const res = await fetch('/api/scores/counts');
            if (!res.ok) return;
            const counts = await res.json();

                        const countMap = {};
            counts.forEach(c => {
                countMap[c.gameId] = c.count || c.playCount || 0;
            });

            const cards = Array.from(grid.querySelectorAll('.game-card'));

                        cards.sort((a, b) => {
                const aLink = a.querySelector('a');
                const bLink = b.querySelector('a');
                const aHref = aLink ? aLink.getAttribute('href') : '';
                const bHref = bLink ? bLink.getAttribute('href') : '';
                const aId = aHref ? aHref.split('/')[2] : '';
                const bId = bHref ? bHref.split('/')[2] : '';

                                const countA = countMap[aId] || 0;
                const countB = countMap[bId] || 0;

                                return countB - countA;
            });

            cards.forEach(card => grid.appendChild(card));
        } catch (err) {
            console.error("Erreur lors du tri des jeux:", err);
        }
    }

