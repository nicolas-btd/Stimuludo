window.LivesManager = {
    setup(container, maxLives, currentLives) {
        if (!container) return;
        container.innerHTML = '';

        if (maxLives === 0) {
            container.innerHTML = '<span>∞</span>';
        } else if (maxLives > 3) {
            container.innerHTML = `<span class="lives-count">${currentLives}</span>`;
            container.appendChild(this.createHeartSVG());
        } else {
            for (let i = 0; i < maxLives; i++) {
                container.appendChild(this.createHeartSVG());
            }
        }
    },

    update(container, currentLives, maxLives) {
        if (!container || maxLives === 0) return;

        if (maxLives > 3) {
            const countSpan = container.querySelector('.lives-count');
            if (countSpan) countSpan.textContent = currentLives;
        } else {
            const hearts = container.querySelectorAll('.heart-icon');
            hearts.forEach((heart, index) => {
                if (index < currentLives) heart.classList.remove('heart-lost');
                else heart.classList.add('heart-lost');
            });
        }
    },

    createHeartSVG() {
        const heart = document.createElementNS("http://www.w3.org/2000/svg", "svg");
        heart.setAttribute("class", "heart-icon");
        heart.setAttribute("viewBox", "0 0 16 16");
        heart.innerHTML = `
            <path class="heart-bg" fill="var(--accent-color-secondary)" d="M8 1.314C12.438-3.248 23.534 4.735 8 15-7.534 4.736 3.562-3.248 8 1.314z"/>
            <path class="heart-overlay" fill="url(#heartGradientOverlay)" d="M8 1.314C12.438-3.248 23.534 4.735 8 15-7.534 4.736 3.562-3.248 8 1.314z"/>
        `;
        return heart;
    }
};

const LanguageManager = {
    currentLang: 'en', 

    init() {

                const savedLang = localStorage.getItem('siteLanguage');
        const userLang = navigator.language || navigator.userLanguage;

        if (savedLang) {
            this.currentLang = savedLang;
        } else if (userLang && userLang.startsWith('fr')) {
            this.currentLang = 'fr';
        } else {
            this.currentLang = 'en';
        }

        this.applyTranslations();

        window.addEventListener('resize', () => {
            this.applyTranslations();
        });
    },

    getNestedTranslation(obj, path) {
        return path.split('.').reduce((prev, curr) => {
            return prev ? prev[curr] : null;
        }, obj);
    },

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        const isMobile = window.innerWidth <= 700;

        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            let text = this.getNestedTranslation(translations[this.currentLang], key);


                        if (!text) {
                text = this.getNestedTranslation(translations['en'], key);
            }

            if (isMobile && key === 'home.search_placeholder') {
                text = this.currentLang === 'fr' ? "Rechercher..." : "Search...";
            }

            if (text) {
                if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
                    el.placeholder = text;
                } else {
                    el.textContent = text;
                }
            }
        });

        document.documentElement.setAttribute('lang', this.currentLang);
    }
};

window.updateTranslations = function () {
    LanguageManager.init();
};

class VirtualNumpad {
    constructor(containerSelector, options = {}) {
        this.container = document.querySelector(containerSelector);
        if (!this.container) return;

        this.onInput = options.onInput || (() => { });
        this.onDelete = options.onDelete || (() => { });
        this.onClear = options.onClear || (() => { });
        this.onSubmit = options.onSubmit || (() => { });

        this.render();
        this.bindEvents();
    }

    render() {
        this.container.innerHTML = `
            <div class="virtual-numpad">
                <button class="vk-btn" data-key="1">1</button>
                <button class="vk-btn" data-key="2">2</button>
                <button class="vk-btn" data-key="3">3</button>
                <button class="vk-btn" data-key="4">4</button>
                <button class="vk-btn" data-key="5">5</button>
                <button class="vk-btn" data-key="6">6</button>
                <button class="vk-btn" data-key="7">7</button>
                <button class="vk-btn" data-key="8">8</button>
                <button class="vk-btn" data-key="9">9</button>
                
                <button class="vk-btn vk-action vk-clear" data-key="clear">C</button>
                <button class="vk-btn" data-key="0">0</button>
                
                <button class="vk-btn vk-action vk-del" data-key="del">
                    <svg width="24" height="24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" viewBox="0 0 24 24">
                        <path d="M21 4H8l-7 8 7 8h13a2 2 0 0 0 2-2V6a2 2 0 0 0-2-2z"></path>
                        <line x1="18" y1="9" x2="12" y2="15"></line>
                        <line x1="12" y1="9" x2="18" y2="15"></line>
                    </svg>
                </button>
            </div>
        `;
    }

    bindEvents() {
        const buttons = this.container.querySelectorAll('.vk-btn');
        buttons.forEach(btn => {
            btn.addEventListener('pointerdown', (e) => {
                e.preventDefault();

                if ("vibrate" in navigator) {
                    try {
                        const canVibrate = window.navigator.userActivation ? window.navigator.userActivation.hasBeenActive : true;
                        if (canVibrate) navigator.vibrate(15);
                    } catch (err) { }
                }

                const key = btn.getAttribute('data-key');

                if (key === 'del') {
                    this.onDelete();
                } else if (key === 'clear') {
                    this.onClear();
                } else if (key === 'enter') {
                    this.onSubmit();
                } else {
                    this.onInput(key);
                }
            });
        });
    }
}
